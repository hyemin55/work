package ex04;

public class ex04 {

	public static void main(String[] args) {
		
		int a = 2;
		
		System.out.println(a>>1);
//		2를 2진수로 만들고 오른쪽으로 한칸이동 했을 때 값이 1이 나온다.
		System.out.println(a<<1);
//		2를 2진수로 만들고 왼쪽으로 3칸 이동 했을 때 값이 16이 나온다.
		
//		print는 같은 줄에 도출값이 표시
//		println는 줄바꿔 도출값을 표시.
		
	}
}

